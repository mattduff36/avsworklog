    const err = error as { message?: string; detail?: string; hint?: string };
    console.error('Error:', err.message);
    if (err.detail) {
      console.error('Details:', err.detail);
    }
    if (err.hint) {
      console.error('Hint:', err.hint);
    }
    
    // Check if tables already exist
